
public class Coordinate {

	int row;
	int column;
	
	public Coordinate(int x, int y) {
		
		row = x;
		column = y;
		
	}
	public int getX() {
		return row;
	}
	public void setX(int x) {
		this.row = x;
	}
	public int getY() {
		return column;
	}
	public void setY(int y) {
		this.column = y;
	}
	
	
	
}
